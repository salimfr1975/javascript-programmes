var jour = 3;
switch(jour){
    case 1:
        document.write("Dimanche");
        break;
    case 2:
        document.write("Lundi");
        break;
    case 3:
        document.write("Mardi");
        break;
    case 4:
        document.write("Mercredi");
        break;
    case 5:
        document.write("Jeudi");
        break;
    case 6:
        document.write("Vandredi");
        break;
    case 7:
        document.write("Samedi");
        break;
    default:
        document.write("Enter valid jour");
        break;
}